public class ManagerList {
    private java.lang.Integer manager_list_id; //managerListId
    private java.lang.String id; //id
    private java.lang.String name; //name
    private java.lang.String avatar; //avatar
    private java.lang.String company_name; //companyName
    private java.lang.String fund_num; //fundNum
    private java.lang.String major_fund_name; //majorFundName
    private java.lang.String data_version; //dataVersion
    private java.lang.Integer proc_status; //procStatus

    public Integer getManager_list_id() {
        return manager_list_id;
    }

    public void setManager_list_id(Integer manager_list_id) {
        this.manager_list_id = manager_list_id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getCompany_name() {
        return company_name;
    }

    public void setCompany_name(String company_name) {
        this.company_name = company_name;
    }

    public String getFund_num() {
        return fund_num;
    }

    public void setFund_num(String fund_num) {
        this.fund_num = fund_num;
    }

    public String getMajor_fund_name() {
        return major_fund_name;
    }

    public void setMajor_fund_name(String major_fund_name) {
        this.major_fund_name = major_fund_name;
    }

    public String getData_version() {
        return data_version;
    }

    public void setData_version(String data_version) {
        this.data_version = data_version;
    }

    public Integer getProc_status() {
        return proc_status;
    }

    public void setProc_status(Integer proc_status) {
        this.proc_status = proc_status;
    }

    @Override
    public String toString() {
        return "ManagerList{" +
                "manager_list_id=" + manager_list_id +
                ", id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", avatar='" + avatar + '\'' +
                ", company_name='" + company_name + '\'' +
                ", fund_num='" + fund_num + '\'' +
                ", major_fund_name='" + major_fund_name + '\'' +
                ", data_version='" + data_version + '\'' +
                ", proc_status=" + proc_status +
                '}';
    }
}
