package net.niuniubao.ant.simuquan.model;

/**
 * Created by xiaoming(a)wallaw.cn on 16/4/25.
 * 基金公司列表数据
 * http://api.quchaogu.com/fund/company/list?page=100
 */
public class CompanyList {

    //json数据
    private String company_id;//公司Id
    private String name;//公司名称
    private String logo;//logo
    private String fund_num;//旗下基金数
    private String major_fund_name;//代表产品名称
    private String major_manager_name;//代表基金经理姓名
    //自定义数据
    private Integer id;
    private String dataVersion;
    private Integer procStatus;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getDataVersion() {
        return dataVersion;
    }

    public void setDataVersion(String dataVersion) {
        this.dataVersion = dataVersion;
    }

    public Integer getProcStatus() {
        return procStatus;
    }

    public void setProcStatus(Integer procStatus) {
        this.procStatus = procStatus;
    }

    public String getCompany_id() {
        return company_id;
    }

    public void setCompany_id(String company_id) {
        this.company_id = company_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getFund_num() {
        return fund_num;
    }

    public void setFund_num(String fund_num) {
        this.fund_num = fund_num;
    }

    public String getMajor_fund_name() {
        return major_fund_name;
    }

    public void setMajor_fund_name(String major_fund_name) {
        this.major_fund_name = major_fund_name;
    }

    public String getMajor_manager_name() {
        return major_manager_name;
    }

    public void setMajor_manager_name(String major_manager_name) {
        this.major_manager_name = major_manager_name;
    }

    @Override
    public String toString() {
        return "CompanyList{" +
                "id=" + id +
                ", company_id='" + company_id + '\'' +
                ", name='" + name + '\'' +
                ", logo='" + logo + '\'' +
                ", fund_num='" + fund_num + '\'' +
                ", major_fund_name='" + major_fund_name + '\'' +
                ", major_manager_name='" + major_manager_name + '\'' +
                ", dataVersion='" + dataVersion + '\'' +
                ", procStatus=" + procStatus +
                '}';
    }

}
