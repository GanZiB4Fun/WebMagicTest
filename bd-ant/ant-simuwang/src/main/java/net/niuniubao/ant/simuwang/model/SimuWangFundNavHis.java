package net.niuniubao.ant.simuwang.model;

/**
 * 私募排排网产品历史净值
 * Created by GanJianWei on 2016/4/16.
 */

public class SimuWangFundNavHis {
    private int navHisId;

    private String htmlId;

    private String priceDate;//净值日期

    private String nav;//单位净值

    private String cumulativeNavWithdrawal;//累计净值(分红再投资)

    private String cumulativeNav;//累计净值(分红不投资)

    private String priceChange;//净值变动

    private String initUnitValue;//初始单位净值

    public int getNavHisId() {
        return navHisId;
    }

    public void setNavHisId(int navHisId) {
        this.navHisId = navHisId;
    }

    public String getPriceDate() {
        return priceDate;
    }

    public void setPriceDate(String priceDate) {
        this.priceDate = priceDate;
    }

    public String getNav() {
        return nav;
    }

    public void setNav(String nav) {
        this.nav = nav;
    }

    public String getCumulativeNavWithdrawal() {
        return cumulativeNavWithdrawal;
    }

    public void setCumulativeNavWithdrawal(String cumulativeNavWithdrawal) {
        this.cumulativeNavWithdrawal = cumulativeNavWithdrawal;
    }

    public String getCumulativeNav() {
        return cumulativeNav;
    }

    public void setCumulativeNav(String cumulativeNav) {
        this.cumulativeNav = cumulativeNav;
    }

    public String getPriceChange() {
        return priceChange;
    }

    public void setPriceChange(String priceChange) {
        this.priceChange = priceChange;
    }

    public String getInitUnitValue() {
        return initUnitValue;
    }

    public void setInitUnitValue(String initUnitValue) {
        this.initUnitValue = initUnitValue;
    }

    public String getHtmlId() {
        return htmlId;
    }

    public void setHtmlId(String htmlId) {
        this.htmlId = htmlId;
    }

    @Override
    public String toString() {
        return "SimuWangFundNavHis{" +
                "navHisId=" + navHisId +
                ", htmlId=" + htmlId +
                ", priceDate='" + priceDate + '\'' +
                ", nav='" + nav + '\'' +
                ", cumulativeNavWithdrawal='" + cumulativeNavWithdrawal + '\'' +
                ", cumulativeNav='" + cumulativeNav + '\'' +
                ", priceChange='" + priceChange + '\'' +
                ", initUnitValue='" + initUnitValue + '\'' +
                '}';
    }
}
