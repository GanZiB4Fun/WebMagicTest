package net.niuniubao.ant.eastmoney.model;

/**
 * Created by design on 2016/5/4.
 */
public class FundJzObj {
    private Integer jz_id; //jzId
    private String fund_url_id; //基金Id
    private String date; //净值日期
    private Double unit_net; //单位净值
    private Double accumulated_net; //累计净值
    private String daily_growth_rate; //日增长率
    private String purchase_status; //申购状态
    private String redemption_status; //赎回状态
    private String feng_hong_song_pei; //分红送配

    public Integer getJz_id() {
        return jz_id;
    }

    public void setJz_id(Integer jz_id) {
        this.jz_id = jz_id;
    }

    public String getFund_url_id() {
        return fund_url_id;
    }

    public void setFund_url_id(String fund_url_id) {
        this.fund_url_id = fund_url_id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public Double getUnit_net() {
        return unit_net;
    }

    public void setUnit_net(Double unit_net) {
        this.unit_net = unit_net;
    }

    public Double getAccumulated_net() {
        return accumulated_net;
    }

    public void setAccumulated_net(Double accumulated_net) {
        this.accumulated_net = accumulated_net;
    }

    public String getDaily_growth_rate() {
        return daily_growth_rate;
    }

    public void setDaily_growth_rate(String daily_growth_rate) {
        this.daily_growth_rate = daily_growth_rate;
    }

    public String getPurchase_status() {
        return purchase_status;
    }

    public void setPurchase_status(String purchase_status) {
        this.purchase_status = purchase_status;
    }

    public String getRedemption_status() {
        return redemption_status;
    }

    public void setRedemption_status(String redemption_status) {
        this.redemption_status = redemption_status;
    }

    public String getFeng_hong_song_pei() {
        return feng_hong_song_pei;
    }

    public void setFeng_hong_song_pei(String feng_hong_song_pei) {
        this.feng_hong_song_pei = feng_hong_song_pei;
    }

    @Override
    public String toString() {
        return "FundJzObj{" +
                "jz_id=" + jz_id +
                ", fund_url_id='" + fund_url_id + '\'' +
                ", date='" + date + '\'' +
                ", unit_net=" + unit_net +
                ", accumulated_net=" + accumulated_net +
                ", daily_growth_rate='" + daily_growth_rate + '\'' +
                ", purchase_status='" + purchase_status + '\'' +
                ", redemption_status='" + redemption_status + '\'' +
                ", feng_hong_song_pei='" + feng_hong_song_pei + '\'' +
                '}';
    }
}

